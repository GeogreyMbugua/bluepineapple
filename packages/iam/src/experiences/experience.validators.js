import { z } from "zod";
export const CreateExperienceSchema = z.object({
    name: z.string().min(1, "Name is required"),
    slug: z.string().min(1, "Slug is required"),
    description: z.string().optional().nullable(),
    shortDescription: z.string().optional().nullable(),
    durationMinutes: z.number().optional().nullable(),
    defaultPrice: z.number().min(0).optional().nullable(),
    currency: z.string().length(3).default("KES"),
    category: z.enum(["TRANSPORT", "LEISURE", "ADVENTURE", "PRIVATE", "CORPORATE"]),
    isFeatured: z.boolean().default(false),
    isActive: z.boolean().default(true),
    heroImageUrl: z.string().url().optional().nullable(),
    galleryUrls: z.array(z.string().url()).default([]),
    maxGroupSize: z.number().optional().nullable(),
    minGroupSize: z.number().optional().nullable(),
    requirements: z.array(z.string()).default([]),
    includes: z.array(z.string()).default([]),
    highlights: z.array(z.string()).default([]),
    meta: z.record(z.string(), z.unknown()).optional(),
});
export const UpdateExperienceSchema = z.object({
    name: z.string().min(1).optional(),
    slug: z.string().min(1).optional(),
    description: z.string().optional().nullable(),
    shortDescription: z.string().optional().nullable(),
    durationMinutes: z.number().optional().nullable(),
    defaultPrice: z.number().min(0).optional().nullable(),
    currency: z.string().length(3).optional(),
    category: z.enum(["TRANSPORT", "LEISURE", "ADVENTURE", "PRIVATE", "CORPORATE"]).optional(),
    isFeatured: z.boolean().optional(),
    isActive: z.boolean().optional(),
    heroImageUrl: z.string().url().optional().nullable(),
    galleryUrls: z.array(z.string().url()).optional(),
    maxGroupSize: z.number().optional().nullable(),
    minGroupSize: z.number().optional().nullable(),
    requirements: z.array(z.string()).optional(),
    includes: z.array(z.string()).optional(),
    highlights: z.array(z.string()).optional(),
    meta: z.record(z.string(), z.unknown()).optional(),
});
export const ExperienceSearchSchema = z.object({
    query: z.string().min(1),
    category: z.enum(["TRANSPORT", "LEISURE", "ADVENTURE", "PRIVATE", "CORPORATE"]).optional(),
});
